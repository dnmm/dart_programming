void main() {
  //# Arithmetic Operators
  var a = 10;
  var b = 20;
  var c = 50;
  var d = 3;

  print(a + b +c); 


  print(c - a);
  print(a * b);
  print(a / d);
  print(a ~/ d);
  print(a % d);

  //# Prefix and Postfix Increment and Decrement operators
  var i = 3;
  print(++i);
  print(i);
  print(i++);
  print(i);
  print(--i);
  print(i);
  print(i--);
  print(i);
  //# Equality and Relational operators
  var a1 = 100;
  var b1 = 200;
  print(a1 == b1);
  print(a1 != b1);
  print(a1 > b1);
  print(a1 < b1);
  print(a1 >= b1);
  print(a1 <= b1);
}
