void main() {
  print('Hello Dart');
  print('Hello' + 'Sonam');
  print('Hello ' + 'Sonam');
  print('Hello' + ' ' + 'Sonam');

  var s1 = 'Hello';
  var s2 = 'Sumit';
  print(s1 + s2);
  print(s1 + ' ' + s2);
  print(s1 + ' My Name is ' + s2);
}
