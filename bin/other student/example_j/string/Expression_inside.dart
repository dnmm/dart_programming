  var name = 'Jinal';
  print('${name}');
  print('$name');
  print('My name is ${name}');
  print('My name is $name');
  print('My name is $name.toUpperCase()');
  print('My name is ${name.toUpperCase()}');
  print('My name is $name and I am good Girl');
}
