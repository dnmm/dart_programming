void main() {
  print("pri increment");
  var i = 3;
  print(i);
  print(++i);
  print(i);

  //post increment
  print("post increment");
  i = 3;
  print(i);
  print(i++);
  print(i);
  //pre dectrement
  print("pre decrement");
  i = 3;
  print(i);
  print(--i);
  print(i);
  //post decrement
  print("post decrement");
  i = 3;
  print(i);
  print(i--);
  print(i);
}
